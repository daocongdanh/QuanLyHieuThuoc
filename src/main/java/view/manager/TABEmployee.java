    private void fillTable() {
        String[] headers = {"Mã nhân viên", "Tên nhân viên", "Email", "Số điện thoại", "Địa chỉ"};
        List<Integer> tableWidths = Arrays.asList(50, 100, 200, 50, 150, 50);
        tableDesign = new TableDesign(headers, tableWidths);
        scrollEmployee.setViewportView(tableDesign.getTable());
        scrollEmployee.setBorder(BorderFactory.createEmptyBorder(15, 20, 20, 20));
        List<Employee> employees = employeeBUS.getAllEmployee();
        fillContent(employees);
    }

    private void addIconFeature() {
        btnAdd.setIcon(ResizeImage.resizeImage(new FlatSVGIcon(getClass().getResource("/img/addBtn.svg")), 35, 35));
        btnUpdate.setIcon(ResizeImage.resizeImage(new FlatSVGIcon(getClass().getResource("/img/editBtn.svg")), 35, 35));
        btnDelete.setIcon(ResizeImage.resizeImage(new FlatSVGIcon(getClass().getResource("/img/deleteBtn.svg")), 35, 35));
    }

    private void fillContent(List<Employee> employees) {
        tableDesign.getModelTable().setRowCount(0);
        for (Employee employee : employees) {
            tableDesign.getModelTable().addRow(new Object[]{employee.getEmployeeId(), employee.getName(), 
            employee.getEmail(), employee.getPhone(), employee.getAddress(), null});
        }
    }

