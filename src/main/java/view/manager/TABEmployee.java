    private void btnSearchEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchEmployeeActionPerformed
        String name = txtSearchEmpName.getText().trim();
        String phone = txtSearchEmpPhone.getText().trim();
        String email = txtSearchEmpEmail.getText().trim();
        
        List<Employee> employees = employeeBUS.getEmployeeByMultipleField(name, phone, email);
        if(employees.size() < 1) {
            MessageDialog.warring(null, "Không tìm thấy nhân viên phù hợp");
        }
        else {
            fillContent(employees);
        }   
    }//GEN-LAST:event_btnSearchEmployeeActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int selectedRow = tableDesign.getTable().getSelectedRow();
        if (selectedRow < 0) {
            MessageDialog.warring(null, "Hãy chọn nhân viên muốn cập nhật thông tin");
        }
        else {
            employeeIdUpdate = (String) tableDesign.getTable().getValueAt(selectedRow, 0);
            
            txtEmployeeName.setText((String) tableDesign.getTable().getValueAt(selectedRow, 1));
            txtEmployeeAddress.setText((String) tableDesign.getTable().getValueAt(selectedRow, 4));
            txtEmployeeEmail.setText((String) tableDesign.getTable().getValueAt(selectedRow, 2));
            txtEmployeePhone.setText((String) tableDesign.getTable().getValueAt(selectedRow, 3));
            
            modalUpdateEmployee.setLocationRelativeTo(null);
            modalUpdateEmployee.setVisible(true);
            if (tableDesign.getTable().getCellEditor() != null) {
                tableDesign.getTable().getCellEditor().stopCellEditing();
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

